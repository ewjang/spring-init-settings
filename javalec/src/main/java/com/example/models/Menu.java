package com.example.models;

public class Menu {

	private int menuNo;
	
	private String menuStep;
	
	private int upMenuNo;
	
	private String menuName;
	
	
	public int getMenuNo() {
		return menuNo;
	}

	public void setMenuNo(int menuNo) {
		this.menuNo = menuNo;
	}

	public String getMenuStep() {
		return menuStep;
	}

	public void setMenuStep(String menuStep) {
		this.menuStep = menuStep;
	}

	public int getUpMenuNo() {
		return upMenuNo;
	}

	public void setUpMenuNo(int upMenuNo) {
		this.upMenuNo = upMenuNo;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	
}
